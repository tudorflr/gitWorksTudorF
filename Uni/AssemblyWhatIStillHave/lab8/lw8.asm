DATA SEGMENT PARA PUBLIC 'DATA'

READ_BUFFER DB 32,?,32 DUP(0)
ADRESS_BUFFER DW READ_BUFFER

DATA ENDS


READ_STRING MACRO X
    PUSH AX
    PUSH DX
    MOV AH,0AH
    MOV DX,X 
    INT 21H
    POP DX
    POP AX
ENDM

PRINT_STRING MACRO X
    MOV SI,X
    INC SI
    XOR AX,AX
    MOV AL,BYTE PTR [SI]
    ADD AL,2
    ADD SI,AX
    MOV BYTE PTR [SI],'$'
    MOV AH,09H
    MOV DX,X 
    ADD DX,2
    INT 21H
ENDM

CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE, DS:DATA
START PROC FAR
PUSH DS
XOR AX, AX
MOV DS, AX
PUSH AX
MOV AX, DATA
MOV DS, AX

READ_STRING ADRESS_BUFFER
PUSH ADRESS_BUFFER
CALL MODIFY_STRING
ADD SP,2
PRINT_STRING ADRESS_BUFFER

RET
START ENDP

MODIFY_STRING PROC NEAR

MOV BP,SP
MOV SI,[BP+2]
ADD SI,2
MOV BYTE PTR [SI],'3'

RET

MODIFY_STRING ENDP

CODE ENDS
END START